    /**
     * bytesToHex method
     */
    static final char[] hexArray = "0123456789ABCDEF".toCharArray();
    private static String bytesToHex(byte[] bytes)
    {
        char[] hexChars = new char[bytes.length * 2];
        for ( int j = 0; j < bytes.length; j++ )
        {
            int v = bytes[j] & 0xFF;
            hexChars[j * 2] = hexArray[v >>> 4];
            hexChars[j * 2 + 1] = hexArray[v & 0x0F];
        }
        return new String(hexChars);
    }

    // Device scan callback.
    private ScanCallback leScanCallback = new ScanCallback() {
        @Override
        public void onScanResult(int callbackType, ScanResult result) {
            final byte[] scanRecord = result.getScanRecord().getBytes();
            int startByte = 2;
            boolean patternFound = false;
            while (startByte <= 5)
            {
                if (    ((int) scanRecord[startByte + 2] & 0xff) == 0x02 && //Identifies an iBeacon
                        ((int) scanRecord[startByte + 3] & 0xff) == 0x15)
                { //Identifies correct data length
                    patternFound = true;
                    break;
                }
                startByte++;
            }

            if (patternFound) {
                //time stamp
                long millis = TimeUnit.MILLISECONDS.convert(result.getTimestampNanos(), TimeUnit.NANOSECONDS);
                Date date = new Date(millis);
                DateFormat formatter = new SimpleDateFormat("yyyy/MM/dd hh:mm.ss.SSS aaa");
                String dateFormatted = formatter.format(date);

                //Convert to hex String
                byte[] uuidBytes = new byte[16];
                System.arraycopy(scanRecord, startByte + 4, uuidBytes, 0, 16);
                String hexString = bytesToHex(uuidBytes);

                //UUID detection
                String uuid =  hexString.substring(0,8) + "-" +
                        hexString.substring(8,12) + "-" +
                        hexString.substring(12,16) + "-" +
                        hexString.substring(16,20) + "-" +
                        hexString.substring(20,32);


                // major
                int major = (scanRecord[startByte + 20] & 0xff) * 0x100 + (scanRecord[startByte + 21] & 0xff);

                // minor
                int minor = (scanRecord[startByte + 22] & 0xff) * 0x100 + (scanRecord[startByte + 23] & 0xff);

                //device name
                String dev_name;
                if(result.getDevice().getName()==null) dev_name="BLE Beacon";
                else dev_name=result.getDevice().getName();

                //
                String ibeacon_data = bytesToHex(scanRecord).substring(0,60);

                peripheralTextView.append("Device Name: " + dev_name + "\n" + "UUID: " + uuid + "\n" + "Major: " + major + "  Minor: " + minor +"\n" + " Timestamp: " + dateFormatted + "\n" + " byte data: " + ibeacon_data + "\n\n");

                File f = new File(filePath);
                //writing to csv
                try {
                    if(f.exists()) {
                        writer = new CSVWriter(new FileWriter(filePath, true));
                    } else {
                        writer = new CSVWriter(new FileWriter(filePath));
                    }

                    String[] data = {dev_name,uuid,Integer.toString(major),Integer.toString(minor),dateFormatted,ibeacon_data};

                    writer.writeNext(data);
                    writer.close();
                    Log.d("Saving complete","Storing to "+ filePath +" done\n");
                }
                catch (IOException e){
                    e.printStackTrace();
                    Log.d("CSVError","Storing to "+ filePath +" error\n");
                }
            }

            // auto scroll for text view
            final int scrollAmount = peripheralTextView.getLayout().getLineTop(peripheralTextView.getLineCount()) - peripheralTextView.getHeight();
            // if there is no need to scroll, scrollAmount will be <=0
            if (scrollAmount > 0)
                peripheralTextView.scrollTo(0, scrollAmount);
        }
    };

